let music1 = document.getElementById("music1")
music1.addEventListener("mouseover",function(event){
    let sound = document.getElementById("Sound1")
    sound.play();
},false);
music1.addEventListener("mouseout",function(event){
    let sound = document.getElementById("Sound1")
    sound.pause();
    sound.currentTime = 0;
})

